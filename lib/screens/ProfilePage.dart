  Widget _getActionButtons() {
    return Padding(
      padding: const EdgeInsets.only(left: 25.0, right: 25.0, top: 45.0),
      child: Row(
        mainAxisSize: MainAxisSize.max,
        mainAxisAlignment: MainAxisAlignment.start,
        children: <Widget>[
          Expanded(
            child: Padding(
              padding: const EdgeInsets.only(right: 10.0),
              child: RaisedButton(
                child: const Text("Save"),
                textColor: Colors.white,
                color: Colors.green,
                onPressed: () async {
                  SharedPreferences sharedPreferences =
                      await SharedPreferences.getInstance();
                  Response response;
                  final Dio dio = Dio();
                  dio.options.headers['content-Type'] = 'application/json';
                  dio.options.headers["authorization"] =
                      "${sharedPreferences.getString('api_access_token')}";
                  response = await dio.post(
                      'http://49.248.144.235/lv/servolutions/api/get_update_user_profile',
                      queryParameters: {
                        'user_id': sharedPreferences.getInt('user_id'),
                        'name': usernameController.text,
                        'contact_number': mobileController.text
                      });
                  if (response.data['status'] == true) {
                    print(response.data);
                    setState(() {
                      _status = true;
                      FocusScope.of(context).requestFocus(FocusNode());
                    });
                    await sharedPreferences.setString(
                        'name', usernameController.text);
                    await sharedPreferences.setString(
                        'contact_number', mobileController.text);
                    final snackBar = SnackBar(
                      content: Text(response.data['message'],
                          style: GoogleFonts.poppins(
                              fontSize: 12.0, color: Colors.white)),
                      backgroundColor: (const Color(0xfffcb913)),
                    );
                    ScaffoldMessenger.of(context).showSnackBar(snackBar);
                  } else {
                    final snackBar = SnackBar(
                      content: Text(response.data['message'],
                          style: GoogleFonts.poppins(
                              fontSize: 12.0, color: Colors.white)),
                      backgroundColor: (const Color(0xfffcb913)),
                    );
                    ScaffoldMessenger.of(context).showSnackBar(snackBar);
                  }
                },
                shape: RoundedRectangleBorder(
                    borderRadius: BorderRadius.circular(20.0)),
              ),
            ),
            flex: 2,
          ),
          Expanded(
            child: Padding(
              padding: const EdgeInsets.only(left: 10.0),
              child: RaisedButton(
                child: const Text("Cancel"),
                textColor: Colors.white,
                color: Colors.red,
                onPressed: () {
                  setState(() {
                    _status = true;
                    FocusScope.of(context).requestFocus(FocusNode());
                  });
                },
                shape: RoundedRectangleBorder(
                    borderRadius: BorderRadius.circular(20.0)),
              ),
            ),
            flex: 2,
          ),
        ],
      ),
    );
  }

